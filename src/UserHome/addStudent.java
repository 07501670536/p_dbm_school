    private void jTextFieldaddStudentTelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldaddStudentTelKeyPressed
          String tel = jTextFieldaddStudentTel.getText();
        int length = tel.length();
        
        char c = evt.getKeyChar();
        
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            
            if (length < 11) {
                jTextFieldaddStudentTel.setEditable(true);
            } else {
                jTextFieldaddStudentTel.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                jTextFieldaddStudentTel.setEditable(true);
                
            } else {
                jTextFieldaddStudentTel.setEditable(false);
            }
            
        }
    }//GEN-LAST:event_jTextFieldaddStudentTelKeyPressed

