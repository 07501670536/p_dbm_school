    public void selectItem() {

        try {
            String query = "SELECT cName FROM cours";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String item = rs.getString("cName");
                jComboBoxaddStudentCoursName.addItem(item);

            }
        } catch (Exception e) {
        }

    }

    public void returnCours() {

        try {
            String query = "SELECT cCost,cOffers,cStart,cEnd FROM cours where cName=?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, (String) jComboBoxaddStudentCoursName.getSelectedItem());
            ResultSet rs = ps.executeQuery();

            if (rs.next() == false) {

                JOptionPane.showMessageDialog(null, "course not avible");

            } else {

                jDateChooserStart.setDate(rs.getDate("cStart"));
                jDateChooserEnd.setDate(rs.getDate("cEnd"));
                jTextFieldaddStudentCoursOffers.setText(rs.getString("cOffers"));
                jTextFieldaddStudentCoursCost.setText(rs.getString("cCost"));

            }

        } catch (Exception e) {

        }

    }

